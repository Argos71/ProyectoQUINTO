    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String usu, pass;
        usu=txtUsuario.getText();
        pass=txtPassword.getText();
        
        String url = "SELECT usuario_personal, password_personal, cargo"
                +" FROM PERSONAL WHERE estado = 1 AND usuario_personal ='"+usu+"'";
        try{
            CConexion objetoConexion = new CConexion();
            objetoConexion.establecerConexion();
            Connection con = objetoConexion.establecerConexion();
            
            PreparedStatement ps = con.prepareStatement(url);
            ResultSet rs = ps.executeQuery();
            
            if(rs.next()){
                //PARA VER SI EXISTE EL USUARIO
                String u = rs.getString("usuario_personal"); //usuario
                String p = rs.getString("password_personal"); //password
                String car = rs.getString("cargo");
                
                if(pass.equals(p)){
                    //VAMOS AL INTERFAZ CORRESPONDIENTE
                    switch (car){
                        case "Secretaria":
                            Secretaria secre = new Secretaria();
                            secre.setVisible(true);
        
                            this.dispose();
                            break;
                        case "Medico":
                            Medico med = new Medico();
                            med.setVisible(true);
        
                            this.dispose();
                            break;
                        case "Nutricionista":
                            Nutricionista nutri = new Nutricionista();
                            nutri.setVisible(true);
        
                            this.dispose();
                            break;
                        case "Entrenador":
                            Entrenador entre = new Entrenador();
                            entre.setVisible(true);
        
                            this.dispose();
                            break;
                        case "Administrador":
                            Administrador admin = new Administrador();
                            admin.setVisible(true);
        
                            this.dispose();
                            break;
                        default:
                            System.out.println("No existe el cargo");
                    }
                }
            }else{
                //SI EL USUARIO NO EXISTE
                JOptionPane.showMessageDialog(null,"EL "
                        + "USUARIO NO EXISTE");
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,"Error al conectar "
                    + "a la base de datos error: "+e.toString());
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

